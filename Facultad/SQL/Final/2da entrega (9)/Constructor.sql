	--VERIFICA QUE NO HAYA COMBINACIONES REPETIDAS QUE LLENEN LA MEMORIA
	CONSTRAINT UQ_RUNAS UNIQUE (ID_RUNA_CLAVE, ID_RUNA_SUP, ID_RUNA_INT, ID_RUNA_INF, ID_RUNA_SEC, ID_RUNA_SEC_ALT)
