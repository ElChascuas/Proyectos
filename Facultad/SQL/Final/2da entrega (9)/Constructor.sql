--VISTAS:
CREATE VIEW VW_WINRATE_CAMPEONES AS
SELECT 
    C.NOMBRE_CAMPEON,
	COUNT(*) AS PARTIDAS_JUGADAS,
	ROUND(CAST(SUM(CASE WHEN R.RESULTADO = 1 THEN 1 ELSE 0 END) AS FLOAT) / COUNT(*) * 100, 2) AS WINRATE
FROM 
    BLNC_CAMPEON.CAMPEON C
JOIN 
    BLNC_CAMPEON.RESUMEN R ON C.ID_CAMPEON = R.ID_CAMPEON
GROUP BY 
    C.NOMBRE_CAMPEON
GO

CREATE VIEW VW_WINRATE_BUILD_OBJETO AS
SELECT 
    O.NOMBRE_OBJETO, 
    O.PRECIO, 
    COUNT(*) AS PARTIDAS_JUGADAS,
    ROUND(CAST(SUM(CASE WHEN B.RESULTADO = 1 THEN 1 ELSE 0 END) AS FLOAT) / COUNT(*) * 100, 2) AS WINRATE
FROM 
    BLNC_MISC.BUILD B
JOIN 
    BLNC_MISC.BUILD_OBJETO BO ON BO.ID_BUILD_OBJETO = B.ID_BUILD_OBJETO
JOIN 
    BLNC_MISC.OBJETO O ON O.ID_OBJETO IN (BO.ID_PRIMER_OBJETO, BO.ID_SEGUNDO_OBJETO, BO.ID_TERCER_OBJETO, 
                                           BO.ID_CUARTO_OBJETO, BO.ID_QUINTO_OBJETO, BO.ID_SEXTO_OBJETO)
GROUP BY 
    O.NOMBRE_OBJETO, O.PRECIO;
GO

CREATE VIEW VW_WINRATE_BUILD_RUNA AS
SELECT 
    R.NOMBRE_RUNA, 
    R.NIVEL, 
    R.RAMA,
    COUNT(*) AS PARTIDAS_JUGADAS,
   ROUND(CAST(SUM(CASE WHEN B.RESULTADO = 1 THEN 1 ELSE 0 END) AS FLOAT) / COUNT(*) * 100, 2) AS WINRATE
FROM 
    BLNC_MISC.BUILD B
JOIN 
    BLNC_MISC.BUILD_RUNA BR ON BR.ID_BUILD_RUNA = B.ID_BUILD_RUNA
JOIN
	BLNC_MISC.RUNA R ON R.ID_RUNA IN (BR.ID_RUNA_CLAVE, BR.ID_RUNA_SUP, BR.ID_RUNA_INT, 
                                           BR.ID_RUNA_INF, BR.ID_RUNA_SEC, BR.ID_RUNA_SEC_ALT)
GROUP BY 
    R.NOMBRE_RUNA, R.NIVEL, R.RAMA;
GO

--STORE PROCEDURES:
CREATE PROCEDURE SP_VERIFICAR_RESULTADOS
AS
BEGIN
    CREATE TABLE #ResultadosErroneos (
        ID_RESUMEN INT,
        ID_CAMPEON INT,
        ID_BUILD INT,
        RESULTADO_RESUMEN BIT,
        RESULTADO_BUILD BIT
    );
    INSERT INTO #ResultadosErroneos (ID_RESUMEN, ID_CAMPEON, ID_BUILD, RESULTADO_RESUMEN, RESULTADO_BUILD)
    SELECT 
        R.ID_RESUMEN,
        R.ID_CAMPEON,
        R.ID_BUILD,
        R.RESULTADO AS RESULTADO_RESUMEN,
        B.RESULTADO AS RESULTADO_BUILD
    FROM 
        BLNC_CAMPEON.RESUMEN R
    INNER JOIN 
        BLNC_MISC.BUILD B ON R.ID_BUILD = B.ID_BUILD AND R.ID_CAMPEON = B.ID_CAMPEON
    WHERE 
        R.RESULTADO <> B.RESULTADO;

    SELECT * FROM #ResultadosErroneos;

    DROP TABLE #ResultadosErroneos;

END;
GO

CREATE PROCEDURE SP_ACTUALIZAR_RESULTADO_RESUMEN
    @ID_RESUMEN INT,
    @RESULTADO BIT
AS
BEGIN
    UPDATE BLNC_CAMPEON.RESUMEN
    SET 
        RESULTADO = @RESULTADO
    WHERE 
        ID_RESUMEN = @ID_RESUMEN;
END
