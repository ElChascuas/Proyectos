CREATE TABLE BLNC_MISC.BUILD (
    ID_BUILD INT PRIMARY KEY,
    ID_CAMPEON INT NOT NULL FOREIGN KEY REFERENCES BLNC_CAMPEON.CAMPEON(ID_CAMPEON),
    ID_BUILD_RUNA INT NOT NULL,
    ID_BUILD_OBJETO INT NOT NULL,
	RESULTADO BIT NOT NULL,

	--VERIFICA QUE NO HAYA COMBINACIONES REPETIDAS QUE LLENEN LA MEMORIA
	CONSTRAINT UQ_BUILD UNIQUE (ID_CAMPEON, ID_BUILD_RUNA, ID_BUILD_OBJETO),
)
GO

CREATE TRIGGER TRG_CHECK_RESULTADO_BUILD
ON BLNC_MISC.BUILD
AFTER INSERT, UPDATE
AS
BEGIN
    IF EXISTS (
        SELECT 1
        FROM inserted i
        JOIN BLNC_CAMPEON.RESUMEN r ON i.ID_BUILD = r.ID_BUILD AND i.ID_CAMPEON = r.ID_CAMPEON
        WHERE r.RESULTADO <> i.RESULTADO
    )
    BEGIN
        RAISERROR ('El resultado en BUILD debe coincidir con el resultado en RESUMEN.', 16, 1);
        ROLLBACK TRANSACTION;
    END
END
GO

CREATE INDEX IDX_BUILD_CAMPEON ON BLNC_MISC.BUILD(ID_CAMPEON);
