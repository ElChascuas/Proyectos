-- TRIGGER QUE VERIFICA QUE LOS NIVELES DE LAS RUNAS SEAN VALIDOS
CREATE TRIGGER TRG_CHECK_RUNA_NIVEL
ON BLNC_MISC.BUILD_RUNA
AFTER INSERT, UPDATE
AS
BEGIN
    -- Verifica que la runa clave tenga el nivel 'Clave'
    IF EXISTS (
        SELECT 1
        FROM inserted i
        WHERE 
            NOT EXISTS (
                SELECT 1 
                FROM BLNC_MISC.RUNA r 
                WHERE r.ID_RUNA = i.ID_RUNA_CLAVE AND r.NIVEL = 'Clave'
            )
    )
    BEGIN
        RAISERROR ('Error: La runa clave no tiene el nivel "Clave".', 16, 1);
        ROLLBACK TRANSACTION;
        RETURN;
    END


    -- Verifica que la runa superior tenga el nivel 'Superior'
    IF EXISTS (
        SELECT 1
        FROM inserted i
        WHERE 
            NOT EXISTS (
                SELECT 1 
                FROM BLNC_MISC.RUNA r 
                WHERE r.ID_RUNA = i.ID_RUNA_SUP AND r.NIVEL = 'Superior'
            )
    )
    BEGIN
        RAISERROR ('Error: La runa superior no tiene el nivel "Superior".', 16, 1);
        ROLLBACK TRANSACTION;
        RETURN;
    END


    -- Verifica que la runa intermedia tenga el nivel 'Intermedia'
    IF EXISTS (
        SELECT 1
        FROM inserted i
        WHERE 
            NOT EXISTS (
                SELECT 1 
                FROM BLNC_MISC.RUNA r 
                WHERE r.ID_RUNA = i.ID_RUNA_INT AND r.NIVEL = 'Intermedia'
            )
    )
    BEGIN
        RAISERROR ('Error: La runa intermedia no tiene el nivel "Intermedia".', 16, 1);
        ROLLBACK TRANSACTION;
        RETURN;
    END


    -- Verifica que la runa inferior tenga el nivel 'Inferior'
    IF EXISTS (
        SELECT 1
        FROM inserted i
        WHERE 
            NOT EXISTS (
                SELECT 1 
                FROM BLNC_MISC.RUNA r 
                WHERE r.ID_RUNA = i.ID_RUNA_INF AND r.NIVEL = 'Inferior'
            )
    )
    BEGIN
        RAISERROR ('Error: La runa inferior no tiene el nivel "Inferior".', 16, 1);
        ROLLBACK TRANSACTION;
        RETURN;
    END


    -- Verifica que las runas secundarias 1 tienen niveles diferentes
    IF EXISTS (
        SELECT 1 
        FROM inserted i
        JOIN BLNC_MISC.RUNA r1 ON r1.ID_RUNA = i.ID_RUNA_SEC
        JOIN BLNC_MISC.RUNA r2 ON r2.ID_RUNA = i.ID_RUNA_SEC_ALT 
        WHERE r1.NIVEL = r2.NIVEL
    )
    BEGIN
        RAISERROR ('Error: Las runas secundarias deben tener niveles diferentes.', 16, 1);
        ROLLBACK TRANSACTION;
        RETURN;
    END


    -- Verificar que ninguna runa secundaria 2 tenga nivel 'Clave'
    IF EXISTS (
        SELECT 1 
        FROM inserted i
        JOIN BLNC_MISC.RUNA r1 ON r1.ID_RUNA = i.ID_RUNA_SEC
        WHERE r1.NIVEL = 'Clave'
    )
    BEGIN
        RAISERROR ('Error: Las runas secundarias no deben tener nivel "Clave".', 16, 1);
        ROLLBACK TRANSACTION;
        RETURN;
    END


    IF EXISTS (
        SELECT 1 
        FROM inserted i
        JOIN BLNC_MISC.RUNA r2 ON r2.ID_RUNA = i.ID_RUNA_SEC_ALT
        WHERE r2.NIVEL = 'Clave'
    )
    BEGIN
        RAISERROR ('Error: Las runas secundarias no deben tener nivel "Clave".', 16, 1);
        ROLLBACK TRANSACTION;
        RETURN;
    END
END
GO

--TRIGGER QUE VERIFICA QUE LAS RAMAS DE LAS RUNAS SEAN VALIDAS
CREATE TRIGGER TRG_CHECK_RUNA_RAMA
ON BLNC_MISC.BUILD_RUNA
AFTER INSERT, UPDATE
AS
BEGIN
    -- Verifica que la runa Clave y Sup compartan rama
    IF EXISTS (
        SELECT 1
        FROM inserted i
        WHERE 
            NOT EXISTS (
                SELECT 1 
                FROM BLNC_MISC.RUNA r 
                WHERE r.ID_RUNA = i.ID_RUNA_CLAVE AND r.RAMA = 
                    (SELECT RAMA FROM BLNC_MISC.RUNA WHERE ID_RUNA = i.ID_RUNA_SUP)
            )
    )
    BEGIN
        RAISERROR ('Error: La runa clave no pertenece a la misma rama que la runa superior.', 16, 1);
        ROLLBACK TRANSACTION;
        RETURN;
    END


    -- Verifica que la runa Clave e INT compartan rama
    IF EXISTS (
        SELECT 1
        FROM inserted i
        WHERE 
            NOT EXISTS (
                SELECT 1 
                FROM BLNC_MISC.RUNA r 
                WHERE r.ID_RUNA = i.ID_RUNA_CLAVE AND r.RAMA = 
                    (SELECT RAMA FROM BLNC_MISC.RUNA WHERE ID_RUNA = i.ID_RUNA_INT)
            )
    )
    BEGIN
        RAISERROR ('Error: La runa clave no pertenece a la misma rama que la runa intermedia.', 16, 1);
        ROLLBACK TRANSACTION;
        RETURN;
    END


    -- Verifica que la runa Clave e Inf compartan rama
    IF EXISTS (
        SELECT 1
        FROM inserted i
        WHERE 
            NOT EXISTS (
                SELECT 1 
                FROM BLNC_MISC.RUNA r 
                WHERE r.ID_RUNA = i.ID_RUNA_CLAVE AND r.RAMA = 
                    (SELECT RAMA FROM BLNC_MISC.RUNA WHERE ID_RUNA = i.ID_RUNA_INF)
            )
    )
    BEGIN
        RAISERROR ('Error: La runa clave no pertenece a la misma rama que la runa inferior.', 16, 1);
        ROLLBACK TRANSACTION;
        RETURN;
    END


    -- Verifica que las runas secundarias compartan rama
    IF NOT EXISTS (
        SELECT 1 
        FROM inserted i
        JOIN BLNC_MISC.RUNA r1 ON r1.ID_RUNA = i.ID_RUNA_SEC
        JOIN BLNC_MISC.RUNA r2 ON r2.ID_RUNA = i.ID_RUNA_SEC_ALT 
        WHERE r1.RAMA = r2.RAMA
    )
    BEGIN
        RAISERROR ('Error: Las runas secundarias deben ser de ramas diferentes.', 16, 1);
        ROLLBACK TRANSACTION;
        RETURN;
    END


    -- Verifica que la runa secundaria 1 no sea clave
    IF EXISTS (
        SELECT 1 
        FROM inserted i
        JOIN BLNC_MISC.RUNA r1 ON r1.ID_RUNA = i.ID_RUNA_SEC
        WHERE r1.RAMA = 
            (SELECT RAMA FROM BLNC_MISC.RUNA WHERE ID_RUNA = i.ID_RUNA_CLAVE)
    )
    BEGIN
        RAISERROR ('Error: La runa secundaria no debe ser de la misma rama que la runa clave.', 16, 1);
        ROLLBACK TRANSACTION;
        RETURN;
    END


    -- Verifica que la runa secundaria 2 no sea clave
    IF EXISTS (
        SELECT 1 
        FROM inserted i
        JOIN BLNC_MISC.RUNA r2 ON r2.ID_RUNA = i.ID_RUNA_SEC_ALT
        WHERE r2.RAMA = 
            (SELECT RAMA FROM BLNC_MISC.RUNA WHERE ID_RUNA = i.ID_RUNA_CLAVE)
    )
    BEGIN
        RAISERROR ('Error: La runa secundaria alternativa no debe ser de la misma rama que la runa clave.', 16, 1);
        ROLLBACK TRANSACTION;
        RETURN;
    END
END
GO

CREATE TABLE BLNC_MISC.BUILD_OBJETO (
    ID_BUILD_OBJETO INT NOT NULL PRIMARY KEY,
    ID_PRIMER_OBJETO INT NOT NULL FOREIGN KEY REFERENCES BLNC_MISC.OBJETO(ID_OBJETO),
    ID_SEGUNDO_OBJETO INT NOT NULL FOREIGN KEY REFERENCES BLNC_MISC.OBJETO(ID_OBJETO),
    ID_TERCER_OBJETO INT NOT NULL FOREIGN KEY REFERENCES BLNC_MISC.OBJETO(ID_OBJETO),
    ID_CUARTO_OBJETO INT NOT NULL FOREIGN KEY REFERENCES BLNC_MISC.OBJETO(ID_OBJETO),
    ID_QUINTO_OBJETO INT NOT NULL FOREIGN KEY REFERENCES BLNC_MISC.OBJETO(ID_OBJETO),
    ID_SEXTO_OBJETO INT NOT NULL FOREIGN KEY REFERENCES BLNC_MISC.OBJETO(ID_OBJETO),

	--VERIFICA QUE LOS OBJETOS DE UNA MISMA ENTRADA NO SE REPITAN ENTRE SI
	CONSTRAINT CHK_OBJETOS_DIFERENTES CHECK (
    ID_PRIMER_OBJETO NOT IN (ID_SEGUNDO_OBJETO, ID_TERCER_OBJETO, ID_CUARTO_OBJETO, ID_QUINTO_OBJETO, ID_SEXTO_OBJETO) AND
    ID_SEGUNDO_OBJETO NOT IN (ID_PRIMER_OBJETO, ID_TERCER_OBJETO, ID_CUARTO_OBJETO, ID_QUINTO_OBJETO, ID_SEXTO_OBJETO) AND
    ID_TERCER_OBJETO NOT IN (ID_PRIMER_OBJETO, ID_SEGUNDO_OBJETO, ID_CUARTO_OBJETO, ID_QUINTO_OBJETO, ID_SEXTO_OBJETO) AND
    ID_CUARTO_OBJETO NOT IN (ID_PRIMER_OBJETO, ID_SEGUNDO_OBJETO, ID_TERCER_OBJETO, ID_QUINTO_OBJETO, ID_SEXTO_OBJETO) AND
    ID_QUINTO_OBJETO NOT IN (ID_PRIMER_OBJETO, ID_SEGUNDO_OBJETO, ID_TERCER_OBJETO, ID_CUARTO_OBJETO, ID_SEXTO_OBJETO) AND
    ID_SEXTO_OBJETO NOT IN (ID_PRIMER_OBJETO, ID_SEGUNDO_OBJETO, ID_TERCER_OBJETO, ID_CUARTO_OBJETO, ID_QUINTO_OBJETO)
	),

	--VERIFICA QUE NO HAYAN COMBINACIONES REPETIDAS DE OBJETOS QUE USEN MEMORIA INDEBIDA
	CONSTRAINT UQ_OBJETOS_UNICOS UNIQUE (ID_PRIMER_OBJETO, ID_SEGUNDO_OBJETO, ID_TERCER_OBJETO, 
		ID_CUARTO_OBJETO, ID_QUINTO_OBJETO, ID_SEXTO_OBJETO)
)
GO

