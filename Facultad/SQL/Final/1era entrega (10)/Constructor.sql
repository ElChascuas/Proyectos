CREATE TABLE Campeon (
    id_campeon INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
    nombre_campeon VARCHAR(30) NOT NULL,
    id_linea VARCHAR(5) NOT NULL,
    rol VARCHAR(10) NOT NULL
);

CREATE TABLE Jugador (
    id_jugador INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
    nombre_jugador VARCHAR(30) NOT NULL,
    nivel_jugador INTEGER NOT NULL
);

CREATE TABLE Item (
    id_item INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
    nombre_item VARCHAR(30) NOT NULL,
    precio INTEGER NOT NULL,
    categoria VARCHAR(20) NOT NULL
);

CREATE TABLE Runa (
    id_runa INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
    nombre_runa VARCHAR(30) NOT NULL,
    rama VARCHAR(30) NOT NULL,
    nivel_runa VARCHAR(30) NOT NULL
);

CREATE TABLE Build (
    id_build INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
    id_primer_itemcore INTEGER, 
    id_segundo_itemcore INTEGER,
    id_tercer_itemcore INTEGER,
    id_cuarto_itemcore INTEGER,
    id_runa_clave INTEGER NOT NULL,
    id_runa_superior INTEGER NOT NULL,
    id_runa_intermedia INTEGER NOT NULL,
    id_runa_inferior INTEGER NOT NULL,
    id_primera_runa_secundaria INTEGER NOT NULL,
    id_segunda_runa_secundaria INTEGER NOT NULL,
    FOREIGN KEY (id_primer_itemcore) REFERENCES Item(id_item),
    FOREIGN KEY (id_segundo_itemcore) REFERENCES Item(id_item),
    FOREIGN KEY (id_tercer_itemcore) REFERENCES Item(id_item),
    FOREIGN KEY (id_cuarto_itemcore) REFERENCES Item(id_item),
    FOREIGN KEY (id_runa_clave) REFERENCES Runa(id_runa),
    FOREIGN KEY (id_runa_superior) REFERENCES Runa(id_runa),
    FOREIGN KEY (id_runa_intermedia) REFERENCES Runa(id_runa),
    FOREIGN KEY (id_runa_inferior) REFERENCES Runa(id_runa),
    FOREIGN KEY (id_primera_runa_secundaria) REFERENCES Runa(id_runa),
    FOREIGN KEY (id_segunda_runa_secundaria) REFERENCES Runa(id_runa)
);

CREATE TABLE Partida (
    Id_entrada INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
    id_partida INTEGER NOT NULL, 
    id_jugador INTEGER NOT NULL,
    id_banned_champ INTEGER,
    id_campeon INTEGER NOT NULL,
    id_build INTEGER NOT NULL,
    resultado VARCHAR(10),
    FOREIGN KEY (id_campeon) REFERENCES Campeon(id_campeon),
    FOREIGN KEY (id_banned_champ) REFERENCES Campeon(id_campeon),
    FOREIGN KEY (id_jugador) REFERENCES Jugador(id_jugador),
    FOREIGN KEY (id_build) REFERENCES Build(id_build)
);


